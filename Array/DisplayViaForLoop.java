
package Array;

public class DisplayViaForLoop {
    
    public static void main(String[] args){
       
        //declaring variable to array
        String[] SeventeenHitSongs = {"Clap", "Maestro", "Very Nice", "Super", "LALALI", "HOT"};
        
        //procedure
        
        for (int i = 0; i < SeventeenHitSongs.length; i++){
            System.out.println(SeventeenHitSongs[i]);
        }
    }
    
}

